package OOPSConceptPart2;

public class FinalKeywordConcept {
   public static void main(String[] args) {
	//Final Keyword is used to define constant value.
	//Final keyword is used to change constant values.
	//It is also used to prevent inheritance...
	  //To prevent method overriding --> declare with final keyword in parent method..
	  //To prevent final class concept -->declare with final keyword in parent class..
	   final int i = 10;
	  // i = 20;
	  // i =30;
	   
	   System.out.println(i);
	   System.out.println("***********************");
	   
}
}
